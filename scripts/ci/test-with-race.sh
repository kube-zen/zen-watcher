# H120: Run tests with race detection for GC primitives
# Ensures concurrency safety of zen-sdk/pkg/gc usage

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "H120: Test with Race Detection (GC Primitives)"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

cd "${REPO_ROOT}/zen-watcher"

# Test packages that use zen-sdk GC primitives
PACKAGES=(
    "./pkg/server/..."
)

FAILED=0

for pkg in "${PACKAGES[@]}"; do
    echo "Testing ${pkg} with race detection..."
    if ! go test -race -timeout 5m "${pkg}"; then
        echo "❌ Race detection failed for ${pkg}"
        FAILED=1
    else
        echo "✅ ${pkg} passed race detection"
    fi
done

echo ""
if [ ${FAILED} -eq 0 ]; then
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo "✅ All tests passed race detection"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    exit 0
else
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo "❌ Race detection found issues"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    exit 1
fi

